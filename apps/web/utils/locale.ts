export type TranslationKey = keyof typeof messages.en

export function normalizeLocale(value: string | null | undefined): AppLocale {
  return supportedLocales.includes(value as AppLocale)
    ? value as AppLocale
    : defaultLocale
}

export function translate(
  locale: AppLocale,
  key: TranslationKey,
  replacements: Record<string, string | number> = {},
) {
  const template = messages[locale][key] || messages[defaultLocale][key] || key

  return Object.entries(replacements).reduce(
    (result, [token, value]) => result.replaceAll(`:${token}`, String(value)),
    template,
  )
}

function enumLabel(
  labels: Record<AppLocale, Record<string, string>>,
  locale: AppLocale,
  code: string | number,
  fallback?: string | null,
) {
  return labels[locale][String(code)] || fallback || String(code)
}

